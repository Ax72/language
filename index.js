document.addEventListener('DOMContentLoaded', () => {
    const languageSelector = document.getElementById('language-selector');
    languageSelector.addEventListener('change', (event) => {
        const language = event.target.value;
        translatePage(language);
    });
});

function translatePage(language) {
    document.querySelectorAll('[data-key]').forEach((element) => {
        const key = element.getAttribute('data-key');
        if (translations[language] && translations[language][key]) {
            element.textContent = translations[language][key];
        }
    });
}
let isBold = false; // Qoraytirilganligini kuzatadi
let isItalic = false; // O'qaytirilganligini kuzatadi

const textElement = document.getElementById('text');
const toggleBtn = document.getElementById('toggleBtn');

toggleBtn.addEventListener('click', function() {
  if (!isBold && !isItalic) {
    // Birinchi bosishda matnni qoraytirish
    textElement.style.fontWeight = 'bold';
    isBold = true;
  } else if (isBold && !isItalic) {
    // Ikkinchi bosishda o'qaytirish
    textElement.style.fontWeight = 'normal'; // Qoraytirishni o'chirish
    textElement.style.fontStyle = 'italic'; // O'qaytirish
    isItalic = true;
  } else if (isBold && isItalic) {
    // Uchinchida yana normal holatga qaytadi
    textElement.style.fontWeight = 'normal';
    textElement.style.fontStyle = 'normal';
    isBold = false;
    isItalic = false;
  }
});
